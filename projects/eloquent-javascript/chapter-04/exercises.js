function range(a,b,c) {
var arr= [];
  if (a === b){
    return []
  }else if (c === undefined){
        for (var i = a; i <= b; i++){
      arr.push(i)
      
    }
  }else{
    for (var i = a; i <= b; i += c){
      arr.push(i);
  }
    } return arr;
}

